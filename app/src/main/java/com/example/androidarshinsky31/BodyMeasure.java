package com.example.androidarshinsky31;

public class BodyMeasure {
    private float weight;
    private int steps;

    public BodyMeasure(float weight, int steps) {
        this.weight = weight;
        this.steps = steps;
    }

    public float getWeight() {
        return weight;
    }

    public int getSteps() {
        return steps;
    }
}
